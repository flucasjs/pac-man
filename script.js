function createBoard() {
    let divSquares = new DocumentFragment();
    for (let i = 0; i < layout.length; i++) {
        let square = document.createElement('div');
        

        if (layout[i] === 0) {
            square.classList.add('pac-dot');
        } else if (layout[i] === 1) {
            square.classList.add('wall');
        } else if (layout[i] === 3) {
            square.classList.add('power-pellet');
        }

        divSquares.appendChild(square);
        squares.push(square);
    }
    grid.appendChild(divSquares);
}

createBoard();

let pacmanStartingIndex = 490;
let pacmanCurrentIndex = pacmanStartingIndex;
squares[pacmanStartingIndex].classList.add('pacman');

function control(e) {
    squares[pacmanCurrentIndex].classList.remove('pacman');
    switch(e.keyCode) {
        case 40:
            if (pacmanCurrentIndex < squares.length - width) {
                pacmanCurrentIndex += width;
            }
        break

        case 38:
            if (pacmanCurrentIndex >= width) {
                pacmanCurrentIndex -= width;
            }
        break

        case 37:
            if (pacmanCurrentIndex % width !== 0) {
                pacmanCurrentIndex--;
            }
        break
        
        case 39:
            if (pacmanCurrentIndex % width < width - 1) {
                pacmanCurrentIndex++;
            }
        break
    }
    squares[pacmanCurrentIndex].classList.add('pacman');
}

document.addEventListener('keyup', control)